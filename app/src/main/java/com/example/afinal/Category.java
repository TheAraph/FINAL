package com.example.afinal;

public class Category {

    private String Name;
    private String Date;
    private String Price;
    private String Description;

    public String getName(){
        return Name;
    }

    public String getDate() {
        return Date;
    }

    public String getPrice() {
        return Price;
    }

    public String getDescription() {
        return Description;
    }
}


